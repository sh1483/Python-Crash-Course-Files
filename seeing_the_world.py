
locations = ['mykonos','ibiza','norway','sweden','phuket']
print(locations)
print(sorted(locations))#temporarily alphabetical order
print(locations)#original order
print(sorted(locations,reverse=True))#sorted reverse order
print(locations)#original order
locations.reverse()
print(locations)#reverses the order
locations.reverse()
print(locations)#back to original order
locations.sort()
print(locations)#permanent alphabetical order
locations.sort(reverse=True)
print(locations)#reverse alphabetical order
